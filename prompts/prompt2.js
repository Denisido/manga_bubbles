const prompt2 = (newsText) => `
Ты сценарист и раскадровщик манга-комикса.

Вот ироничная новость:
---
${newsText}
---

Разбей новость на 4–6 кадров комикса. Для каждого кадра:

1. Опиши сцену:
- Что происходит (фон, персонажи, действия, выражения лиц, обстановка)
- Задай координаты и размеры кадра: "x", "y", "width", "height" для картинки 1024x1024.

2. Добавь поле "speechBubble":
- Это массив объектов (даже если пузырь один), либо null (если ни одного пузыря нет).
- Каждый пузырь содержит:
    - "id" — уникальный идентификатор (bubble1, bubble2 и т.д.)
    - "x", "y" — координаты центра пузыря в пределах кадра
    - "width", "height" — размеры пузыря
    - "text" — содержимое реплики
    - "style" — стиль пузыря: "dialog", "shout", "scream", "whisper", "thought", "robot", "note", "electric" и т.д.
    - "hasTail" — true/false (если есть хвост)
    - "anchorX", "anchorY" — координаты, куда указывает хвост. Если есть персонаж — хвост направлен на него (голова, рот, рука и т.п.), иначе рядом с пузырём или в пустоту. AnchorX/Y всегда в пределах кадра.
    - "bubbleColor", "borderColor" — цвета фона и контура (hex-коды)
    - "irregularity" — неровность контура (от 0 до 0.6)

3. Внимание! Все параметры пузыря ("x", "y", "width", "height") должны быть подобраны так, чтобы пузырь **полностью помещался в пределах кадра**:
    - x - width/2 ≥ frame.x
    - x + width/2 ≤ frame.x + width
    - y - height/2 ≥ frame.y
    - y + height/2 ≤ frame.y + height
    - anchorX и anchorY — всегда внутри области кадра (между frame.x и frame.x + width, frame.y и frame.y + height)
    - Если пузырей несколько, размещай их так, чтобы они не перекрывали друг друга и не закрывали важных объектов сцены (по описанию).

4. Если в кадре **нет пузырей** — укажи: "speechBubble": null (поле должно присутствовать всегда!).

Формат результата:
[
  {
    "scenario": {
      "frame": 1,
      "x": ..., "y": ..., "width": ..., "height": ...,
      "description": "..."
    },
    "speechBubble": [
      {
        "id": "...",
        "x": ..., "y": ..., "width": ..., "height": ...,
        "text": "...",
        "style": "...",
        "anchorX": ..., "anchorY": ...,
        "hasTail": true,
        "bubbleColor": "...",
        "borderColor": "...",
        "irregularity": ...
      }
      // дополнительные пузыри, если они есть
    ]
  },
  ...
]

Не добавляй пояснений, комментариев или пояснительного текста — только валидный JSON-массив!
`;
export default prompt2;